//利用string类里的toUpperCase()和toLowerCase()来实现大小写的转换
public class ToCase {

	public static void main(String[] args) {
		// TODO 自动生成的方法存根
		String str = new String("abc");
		String str2 = new String("ABC");
		System.out.println("第一个字符串为 "+str);
		System.out.println("利用toUpperCase方法后为 "+str.toUpperCase());
		System.out.println("第二个字符串为 "+str2);
		System.out.println("利用toLowerCase方法后为 "+str.toLowerCase());
		
		
		
		

	}

}
